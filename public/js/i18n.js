(function() {
  const savedLang = localStorage.getItem('o2y_lang') || 'tr';
  window.o2yLang = savedLang;

  function applyLang(lang) {
    const t = O2Y_TRANSLATIONS[lang] || O2Y_TRANSLATIONS.tr;
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (t[key]) {
        if (el.tagName === 'INPUT' && el.placeholder) el.placeholder = t[key];
        else el.textContent = t[key];
      }
    });
    document.documentElement.lang = lang;
    window.o2yLang = lang;
    localStorage.setItem('o2y_lang', lang);
    // Update switcher text
    const switchBtn = document.getElementById('langSwitch');
    if (switchBtn) switchBtn.textContent = lang === 'tr' ? 'EN' : 'TR';
  }

  // Apply on load
  document.addEventListener('DOMContentLoaded', () => {
    applyLang(savedLang);
    const switchBtn = document.getElementById('langSwitch');
    if (switchBtn) {
      switchBtn.addEventListener('click', () => {
        const newLang = window.o2yLang === 'tr' ? 'en' : 'tr';
        applyLang(newLang);
      });
    }
  });

  window.o2yApplyLang = applyLang;
  window.o2yT = function(key) {
    const t = O2Y_TRANSLATIONS[window.o2yLang] || O2Y_TRANSLATIONS.tr;
    return t[key] || key;
  };
})();
